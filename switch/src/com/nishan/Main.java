public class Main {

    public static void main(String[] args)
    {
        char charvalue = 'Y';

        switch (charvalue)
        {
            case 'A':
                System.out.println("this is A");
                break;

            case 'B':
                System.out.println("this is B");
                break;

            case 'C':
                System.out.println("this is C");

            case 'D':
                System.out.println("this is D");
                break;

            default:
                    System.out.println("this is nothing");
                    break;
        }
    }
}
